<template>
	<section class="navi">
		<span v-for="(i,item) of items" @click="navi(i)" :class="{'active' : pa == i}">{{i}}</span>
	</section>
</template>


<script>
export default {
	name: 'utilPageNavi',
	
	props: {
		pa: Number,
		ps: Number,
		total: Number,
		target: String
	},

	computed: {
		items: function(){
			return Math.ceil(this.total / this.ps);	
		}
	},

	methods: {
		navi: function(index){
			this.$router.push(`/${this.target}/page/${index}`);
		}
	}
}
</script>

